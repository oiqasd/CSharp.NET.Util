        public override bool ValidateAction(object value, PropertyInfo property)
        {
            string idcardno = value?.ToString();
            if (string.IsNullOrEmpty(idcardno) || (idcardno.Length != 15 && idcardno.Length != 18))
                return false;
            bool nRet = false;
            if (idcardno.Length == 18)
